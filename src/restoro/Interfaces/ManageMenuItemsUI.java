    private void RemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveItemActionPerformed
String inputName = JOptionPane.showInputDialog(null, "Write item name you want to remove:");

if (inputName != null && !inputName.trim().isEmpty()) {
    boolean removed = menu.removeItem(inputName);

    if (removed) {
        JOptionPane.showMessageDialog(null, "Item \"" + inputName + "\" removed successfully.");
    } else {
        JOptionPane.showMessageDialog(null, "The item does not exist.");
    }
} else {
    JOptionPane.showMessageDialog(null, "No name entered.");
}

                }//GEN-LAST:event_RemoveItemActionPerformed

