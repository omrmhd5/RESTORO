    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
try {
                int id = Integer.parseInt(ItemID.getText());
                String name = ItemName.getText();
                String description = ItemDescription.getText();
                double price = Double.parseDouble(Price.getText().replace("$", "").trim());
                String category = Category.getText();

                MenuItem newItem = new MenuItem(id, name, description, price, category);
                menu.addItem(newItem);

                JOptionPane.showMessageDialog(null, "Item added successfully!");

                ItemID.setText("");
                ItemName.setText("");
                ItemDescription.setText("");
                Price.setText("0.00$");
                Category.setText("");

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Invalid ID or Price format!", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_AddActionPerformed

